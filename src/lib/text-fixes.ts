export const normalizeLegacyText = (value: string) => {
  let normalized = value;
  for (const [from, to] of Object.entries(WORD_FIXES)) {
    normalized = normalized.split(from).join(to);
    const fallback = from.replace(/ïŋ―/g, '?');
    if (fallback !== from) {
      normalized = normalized.split(fallback).join(to);
    }
  }
  normalized = normalized.replace(/ ïŋ― /g, ' \u00e9 ');
  normalized = normalized.replace(/ïŋ―:/g, '\u00e9:');
  normalized = normalized.replace(/\?:/g, '\u00e9:');
  normalized = normalized.replace(/ïŋ― muito/g, '\u00c9 muito');
  return normalized;
};
